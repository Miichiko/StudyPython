# Calculando o IMC

# pegue o nome da pessoa.
nome_da_pessoa = "Michiko"

# pegue a altura da pessoa.
altura_da_pessoa = 1.53

# pegue o peso da pessoa
peso_da_pessoa = 55

# agora faça o calculo

calculo_imc = peso_da_pessoa / (altura_da_pessoa * altura_da_pessoa)

print(f'{nome_da_pessoa} seu imc é {calculo_imc:.1f}.') # esse 1f esta me mostrando a casa decimal .5